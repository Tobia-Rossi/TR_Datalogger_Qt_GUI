//------------------------------------------------------------------------------
AboutWindow::AboutWindow(QWidget *parent, const QString name, const QString version, const QString author) :
//------------------------------------------------------------------------------
	QDialog(parent),
	_name(name),
	_version(version),
	_author(author),
	_ui(new Ui::AboutWindow)
{
	_ui->setupUi(this);

	_ui->applicationLabel->setText(_name + "  v" + _version);
	_ui->authorLabel->setText(_author);
}

//------------------------------------------------------------------------------
AboutWindow::~AboutWindow()
//------------------------------------------------------------------------------
{
    delete _ui;
}



